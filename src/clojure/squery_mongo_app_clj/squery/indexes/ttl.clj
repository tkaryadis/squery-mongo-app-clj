(try (drop-collection :testdb.testcoll) (catch Exception e ""))

(insert :testdb.testcoll [{:lastModifiedDate (ISODate) :a 2}, {:a 3}])

(create-index :testdb.testcoll (index [{ "lastModifiedDate" 1}] {"expireAfterSeconds"  10 }))


;;update every 2 seconds => never deleted
(loop []
  (Thread/sleep 2000)
  (update- :testdb.testcoll
           (uq {:lastModifiedDate (ISODate)}))
  (recur))

